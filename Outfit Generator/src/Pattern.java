
public class Pattern {
private String patternName;
	public Pattern(String patternName) {
		this.patternName = patternName;
	}
	public Pattern() {
	}
	public String getPatternName() {
		return patternName;
	}
	public String setPatternName(String patternName) {
		this.patternName = patternName;
		return patternName;
	}
}
